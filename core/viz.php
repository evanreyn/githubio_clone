<h2>Magnetic Reversals and the Earth's Core</h2>
<p>Polarity reversals are intrinsic to the geodynamo. They occur spontaneously, although there is evidence that the overall frequency of reversals may be controlled by the rate of heat loss from the core to the mantle. During a polarity reversal, the magnetic field at Earth's surface is weak and multipolar, in contrast to the strongly-dipolar configuration that prevails during stable polarity times. The Open Earth Systems Project seeks to understand the causes and consequences of magnetic polarity reversals in the Earth's core, and how the surface environment is affected during these events.
</p>
